// 8 Kyu

const twiceAsOld = (dadYearsOld, sonYearsOld) => Math.abs(sonYearsOld * 2 - dadYearsOld);

console.log(twiceAsOld(36, 7));
console.log(twiceAsOld(55, 30));
console.log(twiceAsOld(42, 21));
console.log(twiceAsOld(22, 1));
